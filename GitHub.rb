GitHubのリモートリポジトリ作成とブランチ作成

#(1) リモートリポジトリを作成する
https://github.com/smisu/
Repositoriesタブをクリック
Newボタンをクリック
Repository name を入力

#(2) リモートリポジトリを削除する
Repositoriesタブをクリック
削除するリポジトリを選択
Settingsタブをクリック
Danger ZoneのDelete this repository をクリック（画面下部）
削除するリポジトリ名を入力
smisu/git-practice-81
[I understand the consequences, delete this repository ]
パスワード入力: smisu1234

#(3) ブランチを作成する
https://github.com/git-practice-07
画面左側ブランチ選択ボタンをクリック [master▼]
最下段の青色文字の View all branches　をクリック
画面右側の[New branch]ボタンをクリック
Branch name を入力
または、直接 https://github.com/git-practice-07/branches にアクセスして[New branch]をクリック


